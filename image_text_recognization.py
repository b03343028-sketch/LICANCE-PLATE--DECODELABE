print("YOLO Model Loaded Successfully")

# ==========================================================
# Initialize EasyOCR
# ==========================================================

reader = easyocr.Reader(
    ['en'],
    gpu=torch.cuda.is_available()
)

print("EasyOCR Ready")

# ==========================================================
# Upload Test Image
# ==========================================================

print("Upload a Vehicle Image")

uploaded = files.upload()

image_path = list(uploaded.keys())[0]

print("Image Uploaded:", image_path)

# ==========================================================
# Read Image
# ==========================================================

image = cv2.imread(image_path)

output = image.copy()

# ==========================================================
# Detect License Plate
# ==========================================================

results = model.predict(
    source=image,
    conf=0.30,
    save=False
)

plate_count = 0

recognized_numbers = []

# ==========================================================
# Process Detection
# ==========================================================

for result in results:

    for box in result.boxes:

        plate_count += 1

        x1, y1, x2, y2 = map(int, box.xyxy[0])

        plate = image[y1:y2, x1:x2]

        ocr = reader.readtext(plate)

        plate_text = ""

        for item in ocr:

            plate_text += item[1] + " "

        plate_text = plate_text.strip()

        recognized_numbers.append(plate_text)

        cv2.rectangle(
            output,
            (x1, y1),
            (x2, y2),
            (0,255,0),
            2
        )

        cv2.putText(
            output,
            plate_text,
            (x1, y1-10),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.8,
            (0,255,0),
            2
        )

# ==========================================================
# Show Result
# ==========================================================

print("=" * 60)

print("Detected Plates :", plate_count)

for i, plate in enumerate(recognized_numbers):

    print(f"Plate {i+1}: {plate}")

print("=" * 60)

cv2_imshow(output)

