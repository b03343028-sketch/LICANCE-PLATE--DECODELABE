import os
import cv2
import csv
import torch
import easyocr
import pandas as pd

from ultralytics import YOLO
from google.colab import files

print("="*60)
print("PART 5 : VIDEO LICENSE PLATE RECOGNITION")
print("="*60)

# ==========================================================
# Load Trained Model
# ==========================================================

BEST_MODEL = "/content/LicensePlateProject/LicensePlateDetector/weights/best.pt"

model = YOLO(BEST_MODEL)

reader = easyocr.Reader(
    ['en'],
    gpu=torch.cuda.is_available()
)

# ==========================================================
# Upload Video
# ==========================================================

print("Upload Test Video")

uploaded = files.upload()

video_path = list(uploaded.keys())[0]

print("Video :", video_path)

# ==========================================================
# Open Video
# ==========================================================

cap = cv2.VideoCapture(video_path)

width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
fps = cap.get(cv2.CAP_PROP_FPS)

RESULT_DIR = "/content/LicensePlateProject/results"

os.makedirs(RESULT_DIR, exist_ok=True)

output_video = os.path.join(
    RESULT_DIR,
    "prediction_video.mp4"
)

video_writer = cv2.VideoWriter(
    output_video,
    cv2.VideoWriter_fourcc(*'mp4v'),
    fps,
    (width, height)
)

csv_file = os.path.join(
    RESULT_DIR,
    "recognized_plates.csv"
)

records = []

frame_number = 0

print("Processing Video...")

# ==========================================================
# Process Video Frames
# ==========================================================

while cap.isOpened():

    success, frame = cap.read()

    if not success:
        break

    frame_number += 1

    predictions = model.predict(
        source=frame,
        conf=0.30,
        verbose=False
    )

    for prediction in predictions:

        for box in prediction.boxes:

            x1, y1, x2, y2 = map(
                int,
                box.xyxy[0]
            )

            crop = frame[y1:y2, x1:x2]

            result = reader.readtext(crop)

            plate_text = ""

            for item in result:
                plate_text += item[1] + " "

            plate_text = plate_text.strip()

            records.append([
                frame_number,
                plate_text
            ])

            cv2.rectangle(
                frame,
                (x1, y1),
                (x2, y2),
                (0,255,0),
                2
            )

            cv2.putText(
                frame,
                plate_text,
                (x1, y1-10),
                cv2.FONT_HERSHEY_SIMPLEX,
                0.8,
                (0,255,0),
                2
            )

    video_writer.write(frame)

cap.release()

video_writer.release()

print("Video Processing Completed")

# ==========================================================
# Save CSV File
# ==========================================================

with open(csv_file, "w", newline="") as file:

    writer = csv.writer(file)

    writer.writerow([
        "Frame Number",
        "Recognized Plate"
    ])

    writer.writerows(records)

print("CSV File Saved")

